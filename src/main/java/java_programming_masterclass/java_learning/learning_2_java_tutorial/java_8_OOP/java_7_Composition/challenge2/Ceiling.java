package java_programming_masterclass.java_learning.learning_2_java_tutorial.java_8_OOP.java_7_Composition.challenge2;

public class Ceiling {
    private int Height;
    private int paintedColour;

    public Ceiling(int height, int paintedColour) {
        this.Height = height;
        this.paintedColour = paintedColour;
    }

    public int getHeight() {
        return Height;
    }

    public int getPaintedColour() {
        return paintedColour;
    }
}
